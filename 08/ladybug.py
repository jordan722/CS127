def check(game):
    n = len(game)
    if n < 2:
        if '_' not in game:
            return False
        return True
    if game[0] != '_' and game[0] != game[1]:
        return False
    for i in range(1,n-1):
        if game[i] != '_' and game[i] != game[i-1] and game[i] != game[i+1]:
            return False
    if game[n-1] != '_' and game[n-1] != game[n-2]:
        return False
    return True

