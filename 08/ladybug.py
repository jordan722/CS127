def check_game(game):
    if game.count('_') == 0:
        if check(game):
            return "YES"
        else:
            return "NO"
    checked = ['_']
    for i in game:
        if i not in checked:
            if game.count(i) == 1:
                return "NO"
            checked.append(i)
    return "YES"



